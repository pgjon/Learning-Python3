
print('Dobro, triplo e raiz quadrada de um número'
      ' inserido pelo usuário!\n')

num = int(input('Digite um número: '))

# Calculos
dobro = num * 2
triplo = num * 3
raiz = num ** (1/2)  # Potência elevada a 0,5 equivale a raiz de um número

# Saídas
print(f'\nDobro de {num} = {dobro}')
print(f'Triplo de {num} = {triplo}')
print(f'Raiz de {num} = {raiz}\n')
