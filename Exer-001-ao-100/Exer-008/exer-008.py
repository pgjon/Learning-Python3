
print('Calculadora que converte metros em centímetro e milímetros.')

metros = float(input('Qual valor em metros a ser convertido? '))

centi = float(metros * 100)
mili = float(metros * 1000)

print(f'\nValor em metros: {metros} m')
print(f'Valor em centímetros: {centi} cm')
print(f'Valor em milímetros: {mili} mm\n')
